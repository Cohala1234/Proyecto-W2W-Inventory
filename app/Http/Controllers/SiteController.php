    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  \App\Models\Site  $site
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $siteEdit=Site::findOrFail($id);
        return view('site.edit', compact('siteEdit'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \App\Models\Site  $site
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $dataSite = request()->except(['_token','_method']);
        Site::where('id','=',$id)->update($dataSite);
        return redirect('site')->with('message','El registro se actualizo correctamente');
    
    }

